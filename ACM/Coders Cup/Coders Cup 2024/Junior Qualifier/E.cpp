vector<int> components;
vector<bool> visited;
vector<vector<int>> adj;

void dfs(int u) {
    visited[u] = true;
    for (auto v : adj[u]) {
        if (!visited[v]) {
            dfs(v);
        }
    }
}

void solve() {
    int n, m;
    cin >> n >> m;
    adj.assign(n, vector<int>());
    visited.assign(n, false);
    for (int i = 0; i < m; i++) {
        int u, v;
        cin >> u >> v;
        u--;
        v--;
        adj[u].push_back(v);
        adj[v].push_back(u);
    }
    for (int i = 0; i < n; i++) {
        if (!visited[i]) {
            components.push_back(i + 1);
            dfs(i);
        }
    }
    int k = components.size();
    print(k - 1);
    if (k > 1) {
        for (int i = 0; i < k - 1; i++) {
            print(components[i], components[i + 1]);
        }
    }
}

