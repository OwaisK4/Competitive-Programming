vector<vector<int>> adj;
int removable = 0;

int dfs(int src, int parent) {
    int local = 1;
    for (auto child : adj[src]) {
        if (child == parent)
            continue;
        local += dfs(child, src);
    }
    if (local % 2 == 0 && parent != -1) {
        removable++;
    }
    return local;
}

void solve() {
    int n;
    cin >> n;
    adj.assign(n, vector<int>());
    for (int i = 0; i < n - 1; i++) {
        int a, b;
        cin >> a >> b;
        a--;
        b--;
        adj[a].push_back(b);
        adj[b].push_back(a);
    }
    if (n % 2) {
        cout << -1;
        return;
    }
    dfs(0, -1);
    cout << removable;
}

