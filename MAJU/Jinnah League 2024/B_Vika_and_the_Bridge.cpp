void solve() {
    int n, k;
    cin >> n >> k;
    vector<int> a(n);
    for (int i = 0; i < n; i++) {
        cin >> a[i];
        a[i]--;
    }
    vector<priority_queue<int>> pq(k);
    vector<int> last(k, -1);
    for (int i = 0; i < n; i++) {
        int current = i - last[a[i]] - 1;
        pq[a[i]].push(current);
        last[a[i]] = i;
    }
    for (int i = 0; i < k; i++) {
        int current = n - last[i] - 1;
        pq[i].push(current);
    }
    int answer = INT_MAX;
    for (int i = 0; i < k; i++) {
        if (pq[i].empty())
            continue;
        auto it = pq[i].top();
        pq[i].pop();
        pq[i].push(it / 2);
        pq[i].push(it / 2);
        answer = min(answer, pq[i].top());
    }
    print(answer);
}

