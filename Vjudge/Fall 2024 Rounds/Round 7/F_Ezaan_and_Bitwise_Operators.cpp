int binmult(int a, int b) {
    if (b == 0) {
        return 0;
    }
    int answer = binmult(a, b / 2) % MOD;
    answer = (answer * 2) % MOD;
    if (b & 1) {
        answer = (answer + a) % MOD;
    }
    return answer;
}

void solve() {
    int n;
    cin >> n;
    vector<int> a(n);
    for (int i = 0; i < n; i++) {
        cin >> a[i];
    }
    if (n > 2000) {
        print(0);
        return;
    }
    int answer = 1;
    for (int i = 0; i < n; i++) {
        for (int j = i + 1; j < n; j++) {
            int xr = a[i] ^ a[j];
            answer = binmult(answer, xr);
            answer %= MOD;
        }
    }
    print(answer);
}

